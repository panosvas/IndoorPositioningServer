package com.tsl.positioning.server;

public class SensorInfo {
	
	private String ambientLight;
	private String accelometer;
	private String magnetometer;
	private String gyroscope;
	private String compass;
	private String barometer;
	
	public String getAmbientLight() {
		return ambientLight;
	}
	public void setAmbientLight(String ambientLight) {
		this.ambientLight = ambientLight;
	}
	public String getAccelometer() {
		return accelometer;
	}
	public void setAccelometer(String accelometer) {
		this.accelometer = accelometer;
	}
	public String getMagnetometer() {
		return magnetometer;
	}
	public void setMagnetometer(String magnetometer) {
		this.magnetometer = magnetometer;
	}
	public String getGyroscope() {
		return gyroscope;
	}
	public void setGyroscope(String gyroscope) {
		this.gyroscope = gyroscope;
	}
	public String getCompass() {
		return compass;
	}
	public void setCompass(String compass) {
		this.compass = compass;
	}
	public String getBarometer() {
		return barometer;
	}
	public void setBarometer(String barometer) {
		this.barometer = barometer;
	}
	
	
	

}
